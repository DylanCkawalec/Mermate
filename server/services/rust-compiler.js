'use strict';

/**
 * Rust Compiler — deterministic transpiler from CompilationContext to
 * a standalone Rust binary project.
 *
 * Takes the same shared IR as ts-compiler (facts, plan, TLA+ context)
 * and produces: src/main.rs, Cargo.toml
 *
 * The Rust binary is a pure state machine with:
 *   - Typed enums for each entity's state set
 *   - Event enum for all actions
 *   - Struct with state fields
 *   - dispatch() via match arms
 *   - Invariant assertions returning Result<()>
 *   - main() that runs the event sequence and prints TLA-semantics checklist
 */

const { createHash } = require('node:crypto');
const tlaCompiler = require('./tla-compiler');
const logger = require('../utils/logger');

function _hash16(text) {
  if (!text) return null;
  return createHash('sha256').update(text).digest('hex').slice(0, 16);
}

function _sanitizeId(name) { return tlaCompiler._sanitizeId(name || ''); }

function _pascalCase(text) {
  return String(text || '').replace(/[^a-zA-Z0-9]+/g, ' ').trim()
    .split(/\s+/).filter(Boolean)
    .map(p => p[0].toUpperCase() + p.slice(1))
    .join('') || 'Runtime';
}

function _snakeCase(text) {
  return String(text || '').replace(/[^a-zA-Z0-9]+/g, '_').replace(/^_|_$/g, '').toLowerCase() || 'runtime';
}

function _rustVariant(state) {
  return _pascalCase(state);
}

function _buildEntityMap(facts) {
  const entityMap = {};
  for (const entity of (facts?.entities || [])) {
    entityMap[entity.name] = tlaCompiler.mapEntityToVariable(entity);
  }
  return entityMap;
}

function _buildActions(facts, entityMap) {
  return (facts?.relationships || []).map((rel) => {
    const base = tlaCompiler.mapRelationshipToAction(rel, entityMap);
    return { ...base, eventVariant: _pascalCase(base.actionName), methodName: `handle_${_snakeCase(base.actionName)}` };
  });
}

function _buildInvariants(facts, entityMap) {
  return (facts?.failurePaths || []).map((fp, i) => {
    const base = tlaCompiler.mapFailurePathToInvariant(fp, entityMap, i);
    return { ...base, fnName: `check_${_snakeCase(base.name)}` };
  });
}

function _effectState(effectExpr) {
  const m = String(effectExpr || '').match(/\w+'\s*=\s*"([^"]+)"/);
  return m ? m[1] : 'idle';
}

function _preconditionField(preconditionExpr) {
  const m = String(preconditionExpr || '').match(/([A-Za-z_]\w*)\s*=\s*"([^"]+)"/);
  return m ? { field: _snakeCase(m[1]), state: m[2] } : null;
}

// ---- Rust source generation ------------------------------------------------

function _buildMainRs(ctx) {
  const { structName, stateEntities, actions, invariants, diagramName, moduleName } = ctx;
  const lines = [];
  const date = new Date().toISOString().split('T')[0];

  lines.push(`//! ${structName} — Compiled Rust state machine`);
  lines.push(`//! Generated by MERMATE Architecture Compiler`);
  lines.push(`//! Date: ${date}`);
  lines.push(`//! Diagram: ${diagramName}`);
  lines.push(`//! TLA+ module: ${moduleName}`);
  lines.push(`//! Run: cargo run`);
  lines.push(``);
  lines.push(`use std::fmt;`);
  lines.push(``);

  // ---- State enums ----
  for (const entity of stateEntities) {
    const enumName = `State${_pascalCase(entity.id)}`;
    lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Eq)]`);
    lines.push(`pub enum ${enumName} {`);
    for (const s of entity.states) {
      lines.push(`    ${_rustVariant(s)},`);
    }
    lines.push(`}`);
    lines.push(``);
    lines.push(`impl fmt::Display for ${enumName} {`);
    lines.push(`    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {`);
    lines.push(`        write!(f, "{:?}", self)`);
    lines.push(`    }`);
    lines.push(`}`);
    lines.push(``);
  }

  // ---- Event enum ----
  lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Eq)]`);
  lines.push(`pub enum Event {`);
  for (const action of actions) {
    lines.push(`    ${action.eventVariant},`);
  }
  if (actions.length === 0) lines.push(`    Noop,`);
  lines.push(`}`);
  lines.push(``);

  // ---- Runtime struct ----
  lines.push(`pub struct ${structName} {`);
  for (const entity of stateEntities) {
    lines.push(`    pub ${_snakeCase(entity.id)}: State${_pascalCase(entity.id)},`);
  }
  lines.push(`    step: usize,`);
  lines.push(`    invariant_checks: usize,`);
  lines.push(`}`);
  lines.push(``);

  // ---- impl block ----
  lines.push(`impl ${structName} {`);

  // Constructor
  lines.push(`    pub fn new() -> Self {`);
  lines.push(`        Self {`);
  for (const entity of stateEntities) {
    const initVariant = _rustVariant(entity.states[0] || 'idle');
    lines.push(`            ${_snakeCase(entity.id)}: State${_pascalCase(entity.id)}::${initVariant},`);
  }
  lines.push(`            step: 0,`);
  lines.push(`            invariant_checks: 0,`);
  lines.push(`        }`);
  lines.push(`    }`);
  lines.push(``);

  // dispatch
  lines.push(`    pub fn dispatch(&mut self, event: Event) -> Result<bool, String> {`);
  lines.push(`        let handled = match event {`);
  for (const action of actions) {
    lines.push(`            Event::${action.eventVariant} => self.${action.methodName}(),`);
  }
  if (actions.length === 0) lines.push(`            Event::Noop => false,`);
  lines.push(`        };`);
  lines.push(`        self.assert_all_invariants()?;`);
  lines.push(`        self.step += 1;`);
  lines.push(`        Ok(handled)`);
  lines.push(`    }`);
  lines.push(``);

  // Action handlers
  for (const action of actions) {
    const pre = _preconditionField(action.precondition);
    const fromNext = _effectState(action.fromEffect);
    const toNext = _effectState(action.toEffect);
    const fromEnum = `State${_pascalCase(action.fromId)}`;
    const toEnum = `State${_pascalCase(action.toId)}`;

    lines.push(`    fn ${action.methodName}(&mut self) -> bool {`);
    if (pre) {
      lines.push(`        if self.${_snakeCase(action.fromId)} != ${fromEnum}::${_rustVariant(pre.state)} { return false; }`);
    }
    lines.push(`        self.${_snakeCase(action.fromId)} = ${fromEnum}::${_rustVariant(fromNext)};`);
    lines.push(`        self.${_snakeCase(action.toId)} = ${toEnum}::${_rustVariant(toNext)};`);
    lines.push(`        true`);
    lines.push(`    }`);
    lines.push(``);
  }

  // Type invariant
  lines.push(`    fn assert_type_invariant(&self) -> Result<(), String> {`);
  lines.push(`        // All fields are typed enums — type invariant is enforced by the Rust compiler.`);
  lines.push(`        Ok(())`);
  lines.push(`    }`);
  lines.push(``);

  // Safety invariants
  const entityStateMap = {};
  for (const e of stateEntities) entityStateMap[e.id] = new Set(e.states.map(s => s.toLowerCase()));

  for (const inv of invariants) {
    const m = String(inv.tlaExpr || '').match(/([A-Za-z_]\w*)\s*=\s*"([^"]+)"\s*=>\s*([A-Za-z_]\w*)\s*\/=\s*"([^"]+)"/);
    lines.push(`    fn ${inv.fnName}(&self) -> Result<(), String> {`);
    if (m) {
      const trigId = m[1];
      const trigState = m[2];
      const handId = m[3];
      const handState = m[4];
      const trigHasState = entityStateMap[trigId]?.has(trigState.toLowerCase());
      const handHasState = entityStateMap[handId]?.has(handState.toLowerCase());

      if (trigHasState && handHasState) {
        lines.push(`        if self.${_snakeCase(trigId)} == State${_pascalCase(trigId)}::${_rustVariant(trigState)} && self.${_snakeCase(handId)} == State${_pascalCase(handId)}::${_rustVariant(handState)} {`);
        lines.push(`            return Err(format!("${inv.name}: {}={} requires {}!={}", "${trigId}", "${trigState}", "${handId}", "${handState}"));`);
        lines.push(`        }`);
      } else {
        lines.push(`        // Invariant references state not in entity's domain — vacuously true`);
      }
    }
    lines.push(`        Ok(())`);
    lines.push(`    }`);
    lines.push(``);
  }

  // Combined invariant check
  lines.push(`    fn assert_all_invariants(&mut self) -> Result<(), String> {`);
  lines.push(`        self.assert_type_invariant()?;`);
  for (const inv of invariants) {
    lines.push(`        self.${inv.fnName}()?;`);
  }
  lines.push(`        self.invariant_checks += 1;`);
  lines.push(`        Ok(())`);
  lines.push(`    }`);
  lines.push(``);

  // Print state
  lines.push(`    pub fn print_state(&self) {`);
  for (const entity of stateEntities) {
    lines.push(`        println!("  {} = {}", "${entity.id}", self.${_snakeCase(entity.id)});`);
  }
  lines.push(`    }`);
  lines.push(`}`);
  lines.push(``);

  // ---- main() ----
  lines.push(`fn main() {`);
  lines.push(`    println!("\\n============================================");`);
  lines.push(`    println!("  ${structName} — Architecture State Machine");`);
  lines.push(`    println!("  TLA+ Module: ${moduleName}");`);
  lines.push(`    println!("============================================\\n");`);
  lines.push(``);
  lines.push(`    let mut runtime = ${structName}::new();`);
  lines.push(`    println!("[INIT] Initial state:");`);
  lines.push(`    runtime.print_state();`);
  lines.push(`    println!();`);
  lines.push(``);

  // Run all events in order
  lines.push(`    let events = vec![`);
  for (const action of actions) {
    lines.push(`        ("${action.eventVariant}", Event::${action.eventVariant}),`);
  }
  lines.push(`    ];`);
  lines.push(``);
  lines.push(`    let mut passed = 0usize;`);
  lines.push(`    let mut failed = 0usize;`);
  lines.push(``);
  lines.push(`    for (name, event) in &events {`);
  lines.push(`        match runtime.dispatch(*event) {`);
  lines.push(`            Ok(handled) => {`);
  lines.push(`                let sym = if handled { "\\u{2713}" } else { "\\u{2192}" };`);
  lines.push(`                println!("[{}] {} (handled={})", sym, name, handled);`);
  lines.push(`                passed += 1;`);
  lines.push(`            }`);
  lines.push(`            Err(e) => {`);
  lines.push(`                println!("[\\u{2717}] {} FAILED: {}", name, e);`);
  lines.push(`                failed += 1;`);
  lines.push(`            }`);
  lines.push(`        }`);
  lines.push(`    }`);
  lines.push(``);
  lines.push(`    println!();`);
  lines.push(`    println!("============================================");`);
  lines.push(`    println!("  TLA+ SEMANTICS CHECKLIST");`);
  lines.push(`    println!("============================================");`);
  lines.push(`    println!("  Actions:    {}/{}", passed, events.len());`);
  lines.push(`    println!("  Invariants: {} checks passed", runtime.invariant_checks);`);
  lines.push(`    println!("  Steps:      {}", runtime.step);`);
  lines.push(`    println!("  Failures:   {}", failed);`);
  lines.push(`    println!();`);
  lines.push(`    println!("[FINAL] State:");`);
  lines.push(`    runtime.print_state();`);
  lines.push(`    println!();`);
  lines.push(``);
  lines.push(`    if failed == 0 {`);
  lines.push(`        println!("MIRACLE ACHIEVED — all invariants hold, binary verified.");`);
  lines.push(`    } else {`);
  lines.push(`        println!("INVARIANT VIOLATIONS DETECTED — {} failures.", failed);`);
  lines.push(`        std::process::exit(1);`);
  lines.push(`    }`);
  lines.push(`}`);

  return lines.join('\n');
}

function _buildCargoToml(ctx) {
  const pkgName = _snakeCase(ctx.diagramName).replace(/_+/g, '-') || 'mermate-runtime';
  return [
    `[package]`,
    `name = "${pkgName}"`,
    `version = "1.0.0"`,
    `edition = "2021"`,
    `description = "Architecture state machine compiled by MERMATE from TLA+ spec"`,
    ``,
    `[[bin]]`,
    `name = "${pkgName}"`,
    `path = "src/main.rs"`,
    ``,
    `[profile.release]`,
    `opt-level = 3`,
    `lto = true`,
    `strip = true`,
  ].join('\n');
}

// ---- Public API ------------------------------------------------------------

function compileToRust(context) {
  const facts = context?.facts || { entities: [], relationships: [], failurePaths: [] };
  const plan = context?.plan || { nodes: [], edges: [], subgraphs: [] };
  const runId = context?.runId || null;
  const moduleName = context?.moduleName || 'Spec';
  const diagramName = context?.diagramName || 'runtime';
  const tlaSource = context?.tla?.source || '';

  const entityMap = _buildEntityMap(facts);
  const stateEntities = Object.values(entityMap).filter(e => e.isStateful);
  const actions = _buildActions(facts, entityMap);
  const invariants = _buildInvariants(facts, entityMap);

  const structBase = _pascalCase(diagramName);
  const structName = structBase.endsWith('Runtime') ? structBase : `${structBase}Runtime`;

  const mainRs = _buildMainRs({ structName, stateEntities, actions, invariants, diagramName, moduleName });
  const cargoToml = _buildCargoToml({ diagramName });

  const metrics = {
    entityCount: stateEntities.length,
    actionCount: actions.length,
    invariantCount: invariants.length,
    stateSpaceEstimate: stateEntities.reduce((acc, e) => acc * Math.max(e.states.length, 1), 1),
    rustLines: mainRs.split('\n').length,
    tlaHash: _hash16(tlaSource),
  };

  logger.info('rust_compiler.compiled', {
    runId: runId ? String(runId).slice(0, 8) : null,
    structName,
    entities: metrics.entityCount,
    actions: metrics.actionCount,
    invariants: metrics.invariantCount,
    lines: metrics.rustLines,
  });

  return {
    structName,
    mainRs,
    cargoToml,
    metrics,
    artifacts: { mainRs: 'src/main.rs', cargoToml: 'Cargo.toml' },
  };
}

module.exports = { compileToRust, _buildEntityMap, _buildActions, _buildInvariants };
